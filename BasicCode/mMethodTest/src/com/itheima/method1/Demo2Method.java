package com.itheima.method1;

public class Demo2Method {
    public static void main(String[] args) {
        method();
    }

    public static void method() {
        int num = 10;
        if (num % 2 == 0) {
            System.out.println("偶数");
        } else {
            System.out.println("奇数");
        }
    }
}
